                        <p >  The nucleus of the SSNMMH was laid in the year 1952 by starting a small unit with minimum conveniences and with meager resources.Since then, the insitritution has grown up into a major hospital with 350 beds with almost all the specialities available in a modern hospital.The Hospital has attached with School of Nursing and College of Nursing running ANM,GNM,Bsc Nursing post basic nursing and Msc Nursing courses.

                        <p/>
                        <p>The institution is run on philanthropic basis without any profit motive and it renders services to the general public without any distinction of religion,caste,creed,or color.The administration of the Hospital is vested in managing committiee consisting of 11 members nominated by Dharma Sanghaom Trust.We do a lot of chariotable activities. Such as


                        <ul>
                            <li>Providing free treatment for eligible people i,e socially and economically backward patients.</li>
                            <li>Concessions in treatment are given to socially and economically backward patients.</li>
                            <li>Conducting free medical camps on a regular basis.</li>
                            <li>Conducting free cataract surgeries for poor patient in association with different NGOs.</li>
                            <li>Providing free of charge school health care facilities for children.</li>
                        </ul>
                        </p>
                        <p>Our hospital is a 350 bedded Super Specialty Hospital.Our strength is the team of about 50 highly qualified and well experienced and dedicated senior doctors who are ably assisted by about 250 registered nurses and para medical staff with the focus on delivering most modern health care services.Doctors,Nurses and Paramedical staff are all holding a valid registration from with their respective authority i.e Medical Council of India ,The Kerala Nurses and Midwives Council,Indian Nursing Council etc. The Hospital is approved by the Municipal Authority.
                        </p>
                        <p>Our hospital is also equipped with 24 hour Trauma Care facility and high tech Operation theatre,Intensive Care Unit and Intensive Coronary Unit.The Emergency Department undertake all kinds of emergencies,including medical,surgical,orthopedics and pediatric.We have also a full pledged Ambulance Service. The Hospital is recognized by Ministry of Health and Family Welfare Government of India.
                        </p>
                        <p>In this connection we wish to inform you that we have started the new Haemodialysis unit in our hospital in the year 2012.At present we have 8 nos oF B Barun Haemodialysis machine in our hospital and the unit is functioning with full pledged facilities such as well qualified doctors and technicians.
                        </p>
                        <p>To add further The Kerala State Government have also accredited our Hospital(No 213/PH/TVM) under the Karunya Benevolent fund Scheme for the treatment package approved by the sate level committiee under the Head Nephrology maintainnce of Haemodialyisis for chronic kidney disease.
                        </p>
