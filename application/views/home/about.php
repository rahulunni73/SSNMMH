                        <h3 class="main-heading1"><br>History</h3>
                        <p style="text-indent: 50px;">
                            The idea of starting a hospital under the auspices of Sivagiri Mutt had its own origin in Sree Narayana Guru himself and it is said that when a land on which the hospital now stands was acquired by the Mutt ago, the Guru himself had earmarked it for this purpose. But, consequent on the Guru's attaining Samadhi not long after, the project had to lie over for a number of years. The first step implementing the idea was however, taken on 12-2-1951, when the Sree Narayana Dharma Sanghom, at its meeting, resolved that a Medical Mission be founded in the name of Sree Narayana Guru and that Travancore Devaswom board be requested to sanction a liberal grant for the construction of a ward, and its equipment.  
                        </p>
                        <p style="text-indent: 50px;">
                            A ward with 24 beds and a dispensary were opened on first January 1954, by His Highness the Elaya Rajah of Travancore in the presence of a large and distinguished gathering. As the hospital became a very popular one in a very short time, the necessity of further developments was felt by the mission. After that, there has been a strong and steady progress in the growth of the hospital. The Medical Mission had started Auxiliary Nurse Midwifery School in 1964 with an intake of 15 students. Subsequently it was converted as School of Nursing in 1980 imparting GNM course with an intake of 10 students. Later the seat was enhanced to 40. Health Worker School was also started in July 1981 with an intake of 10 and later enhanced to 35.
                        </p>
                        <p style="text-indent: 50px;">
                            It has now grown up to the level of a well equipped super specialty hospital. It has also a college of Nursing with B.Sc Nursing, Post Basic B.Sc (Nursing) and M.Sc (Nursing) Course in five specialties. To cater to the growing needs of the people, we have constructed a seven storied building in the hospital compound and equipped with all modern equipment and amenities. Our ambition is to raise it as a Medical College.
                            There is now Super Specialty facilities in the hospital like cardiology (with Cath lab and coronarycare Unit) Nephrology department with a modern dialysis unit, Urology and Urosurgery department with most modern equipments (Laser Lithotripsy), Neurology department (with a well equipped Neuro Lab with EEG, EMG etc) Gastroenterology department (with endoscopy and colonoscopy equipments), Ophthalmology department with a separate Operation Theatre complex and Lasik treatment facilities, Department of Radio diagnosis with CT scan, Digital X-ray, Ultrasound etc. State of the art and most modern theatre complex is one of the strong hold of the hospital. Round the clock emergency care is provided by the Emergency Department.
                        </p> 
